<li><strong><font color="red">The Health and Demographic Surveillance System (HDSS) Project</strong></font></li>
<p>The Pan-African Society of Hypertension  Initiative is also conducting several DSS studies which help to monitor  longitudinal cohorts on which cardiovascular studies are performed. On this  endeavor, the initiative collaborates with experts in the field like <a href="http://www.indepth-network.org/"> The International Network for the Demographic Evaluation of  Populations and Their Health (INDEPTH)</a>.<br>
  The International  Network for the Demographic Evaluation of Populations and Their Health  (INDEPTH) is a global network of members who conduct longitudinal health and  demographic evaluation of populations in low- and middle-income countries  (LMICs). INDEPTH aims to strengthen global capacity for Health and Demographic  Surveillance Systems (HDSSs), and to mount multi-site research to guide health  priorities and policies in LMICs, based on up-to-date scientific evidence. </p>
