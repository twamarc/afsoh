<h3>About us</h3>
<p>The IFHA is the umbrella organisation for all Scientists involved in cardiovascular in general and hypertension in particular field (individuals, institutions or societies) all over the world. The official membership is obtain after registration. 
Currently the IFHA has a total of more than 100 very active members.</p>
<p>The International Forum for Hypertension control and prevention in Africa (IFHA) was inaugurated in Brussels in May 2003. A first meeting gathered experts from various institutions. 
This was during an international conference organized jointly by European Union through ULB- Hôpital Erasme and the African section of the World Health Organization, the World Hypertension League (WHL), the Belgian Universities (Gent University and KUL Leuven), the Centers for Diseases Control (CDC) and the African Union (AU) with the objectives of setting up specific and priority strategies aimed at curbing the emerging epidemic of CVD in SSA using an international partnership support. </p>
<p>The IFHA has sprung into action and has published the first recommendation
  for the management of hypertension in SSA (Journal of Hypertension 2003; 21:
  1-8).</p>
